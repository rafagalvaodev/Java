    private void jbtnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnVerificarActionPerformed
        // TODO add your handling code here:
        jpainelResultado.setVisible(true);
        int a = jsliA.getValue();
        int b = jsliB.getValue();
        int c = jsliC.getValue();
        if(a<b+c && b<a+c && c<a+b){
            jlblForma.setText("Formam um Tri√¢ngulo");
            if(a==b && b==c){
                jlblTriangulos.setText("Equil√°tero");
            }else if(a!=b && b!=c && c!=a){
                jlblTriangulos.setText("Escaleno");
            }else{
                jlblTriangulos.setText("Is√≥sceles");
            }
        }else{
            jlblForma.setText("N√£o Formam um Tri√¢ngulo");
            jlblTriangulos.setText("_");
        }
        
    }//GEN-LAST:event_jbtnVerificarActionPerformed

